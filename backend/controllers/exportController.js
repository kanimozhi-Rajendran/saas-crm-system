// ─────────────────────────────────────────────────────────────
//  Export Controller — CSV & PDF Report Generation
// ─────────────────────────────────────────────────────────────
const Lead = require("../models/Lead");
const Deal = require("../models/Deal");
const { Parser } = require("json2csv");
const PDFDocument = require("pdfkit");

// ── @route  GET /api/export/leads/csv ─────────────────────────
const exportLeadsCSV = async (req, res, next) => {
  try {
    const leads = await Lead.find()
      .populate("assignedTo", "name")
      .populate("createdBy", "name")
      .lean();

    const fields = [
      { label: "Name", value: "name" },
      { label: "Email", value: "email" },
      { label: "Company", value: "company" },
      { label: "Budget", value: "budget" },
      { label: "Company Size", value: "companySize" },
      { label: "AI Score", value: "leadScore" },
      { label: "Category", value: "leadCategory" },
      { label: "Status", value: "status" },
      { label: "Source", value: "source" },
      { label: "Assigned To", value: "assignedTo.name" },
      { label: "Created At", value: "createdAt" },
    ];

    const parser = new Parser({ fields });
    const csv = parser.parse(leads);

    res.header("Content-Type", "text/csv");
    res.header("Content-Disposition", "attachment; filename=leads.csv");
    res.send(csv);
  } catch (error) {
    next(error);
  }
};

