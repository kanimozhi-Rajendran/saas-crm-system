// ── @route  GET /api/export/analytics/pdf ─────────────────────
const exportAnalyticsPDF = async (req, res, next) => {
  try {
    // Fetch summary data
    const [totalLeads, totalDeals, totalRevenue] = await Promise.all([
      Lead.countDocuments(),
      Deal.countDocuments(),
      Deal.aggregate([
        { $match: { stage: "Closed Won" } },
        { $group: { _id: null, total: { $sum: "$value" } } },
      ]),
    ]);

    const topLeads = await Lead.find().sort({ leadScore: -1 }).limit(10).select("name company leadScore");

    // Create PDF
    const doc = new PDFDocument({ margin: 50 });

    res.header("Content-Type", "application/pdf");
    res.header("Content-Disposition", "attachment; filename=analytics-report.pdf");

    doc.pipe(res);

    // Header
    doc.fontSize(24).fillColor("#00d4ff").text("AI SaaS CRM", { align: "center" });
    doc.fontSize(16).fillColor("#000000").text("Analytics Report", { align: "center" });
    doc.moveDown();
    doc.fontSize(10).fillColor("#666666").text(`Generated: ${new Date().toLocaleString()}`, { align: "center" });
    doc.moveDown(2);

    // Overview Section
    doc.fontSize(18).fillColor("#000000").text("Overview", { underline: true });
    doc.moveDown();
    doc.fontSize(12);
    doc.text(`Total Leads: ${totalLeads}`);
    doc.text(`Total Deals: ${totalDeals}`);
    doc.text(`Total Revenue: $${(totalRevenue[0]?.total || 0).toLocaleString()}`);
    doc.moveDown(2);

    // Top Leads Section
    doc.fontSize(18).text("Top 10 Leads by AI Score", { underline: true });
    doc.moveDown();
    doc.fontSize(10);

    topLeads.forEach((lead, i) => {
      doc.text(`${i + 1}. ${lead.name} (${lead.company || "N/A"}) - Score: ${lead.leadScore}`);
    });

    doc.moveDown(2);

    // Footer
    doc.fontSize(8).fillColor("#999999").text(
      "This report was generated by AI SaaS CRM System",
      50,
      doc.page.height - 50,
      { align: "center" }
    );

    doc.end();
  } catch (error) {
    next(error);
  }
};

module.exports = { exportLeadsCSV, exportDealsCSV, exportAnalyticsPDF };
